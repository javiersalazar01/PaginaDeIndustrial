Con el tema de la puesta en operación de un sistema de entrenamiento de control de procesos y evaluación de comprobadores industriales, la académica María Elena Anaya Pérez obtuvo el Doctorado en Ingeniería Mecatrónica en la Universidad Popular Autónoma del Estado de Puebla. <br><br>

La académica del Departamento de Ingeniería Industrial dio a conocer que, con base en la temática desarrollada en su examen de grado, se puso en operación un equipo de tanques interconectados en el Laboratorio de Control de esa unidad académica, y luego se dio a la tarea de trabajar con el diseño de dos modelos de controladores: uno por la rama teórica y otro por la empírica.<br> <br>

