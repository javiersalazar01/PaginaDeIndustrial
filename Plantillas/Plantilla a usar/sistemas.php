		<div id="descripcion">
			<h2>Descripción General del Plan de Estudios</h2>
			<p>El plan de estudios está compuesto de 44 asignaturas obligatorias, 6 Talleres y 5 Asignaturas optativas a seleccionar de 10 propuestas.</p>
			<p>Las 55 Asignaturas que integran el plan de estudios se distribuyen dentro de 6 áreas curriculares principales. Las 2 áreas principales son: La de Programación y Computación, y la de Sistemas de Información.</p>
			<p>El total de créditos del plan es de 400, mismos que corresponden a 156 horas semana mes de teoría y 86 horas semana mes de prácticas o laboratorio. De la totalidad de créditos, 360 corresponden a materias de carácter obligatorio y 40 a materias optativas.</p>
			<p>Dentro de la currícula transversal del plan de estudios, se integrarán los elementos que complementan el perfil de nuestros egresados, durante el:</p>
			<p>Primero y segundo semestre, el valor a desarrollar será la puntualidad y la responsabilidad, esto se realizará a través de los maestros de estos semestres, promoviendo sean exigentes en las horas de inicio y final de las clases, y estrictos en los tiempos de exámenes y recepción de trabajos y tareas. </p>
			<p>Tercero y cuarto semestre, se fomentarán las actividades extracurriculares, buscando que los alumnos participen en actividades deportivas y culturales, promoviendo el acercamiento a la cultura en general, por medio de pláticas, conferencias, etcétera. </p>
			<p>Quinto y sexto semestre, se promocionará la creatividad, desarrollando un concurso anual de desarrollo de aplicaciones creativas y modelos prototipo relacionados con la solución de problemas de la sociedad. </p>
			<p>Séptimo y octavo semestre, se puntualizará sobre el auto-negocio y el espíritu emprendedor, integrándolos a los programas de Desem (IMPULSA), promoviendo concursos, conferencias, talleres e interacción con empresas de la localidad y el extranjero. </p>
			<p>Además de lo anterior, para lograr su completo desarrollo, los estudiantes contarán con el apoyo de tutores, que los guíen, los supervisen y les den seguimiento a lo largo de su formación profesional; este programa de tutorías deberá ser acorde a las políticas institucionales.</p>
