         <p class="text-justify">
         Cuatro estudiantes del quinto semestre de la carrera de Ingeniería Mecatrónica de nuestra casa de estudios obtuvieron el segundo lugar en el XXV Certamen Nacional de Prototipos Científicos y Tecnológicos, evento realizado del 6 al 9 del mes en curso, y cuya sede fue la Escuela Politécnica de Guadalajara.<br><br>

Con su propuesta, los estudiantes René Salido Noriega, Eduardo Fernández Vega, Giancarlo Álvarez León y José Ángel Corte Tarazón lograron esa posición entre 32 proyectos participantes dentro de la categoría de enseñanza superior.<br><br>

Gonzalo Moroyoqui Estrella, encargado de los laboratorios de Mecatrónica, informó que en el certamen participaron 94 equipos procedentes de 14 estados del país, donde también se incluyó la categoría de enseñanza media superior.<br><br>

Dio a conocer que el concurso --en el que también participaron con otro proyecto--, busca promover la participación de estudiantes de nivel medio superior y superior en el diseño y desarrollo de prototipos didácticos, experimentales y tecnológicos sobre el tema de la luz.<br><br>

Citó que los proyectos están relacionados con la determinación experimental de la conservación del momento lineal en un impacto central directo --el ganador del segundo lugar--, y el otro, con la comprobación experimental de la conservación de la energía utilizando una catapulta de contrapeso.<br><br>

Moroyoqui Estrella consideró que para los alumnos, el hecho de participar en el concurso les ayuda a reforzar los conocimientos que adquieren en el aula, en cuanto a la aplicación de algunos principios físicos.<br><br>

"Por supuesto que asistir a ese tipo de eventos les permite compartir experiencias con estudiantes de diversas universidades y valorar qué es lo que tenemos, qué estamos haciendo bien, en qué aspectos podemos mejorar, porque ese contacto ayuda a compararnos con otras universidades a nivel nacional", expresó el académico.<br><br>

Dijo que las dos propuestas son de tipo didáctico porque pueden utilizarse en el desarrollo de prácticas de diversas asignaturas, además de que su participación fue muy relevante por los conocimientos que aplicaron en cada proyecto.<br><br>

Para René Salido Noriega, su participación fue toda una experiencia por ser la primera vez que lo hacen a ese nivel, "algo totalmente diferente a las cosas que hemos hecho aquí"; a su vez, Eduardo Fernández afirmó que "podemos escalar a mejores resultados y aplicar, a través de proyectos, nuestros conocimiento en algo real y tangible".<br><br>

De igual forma, Giancarlo Álvarez y José Ángel Corte sostuvieron que esa experiencia y aprendizaje fue muy gratificante porque, además, pusieron en alto el nombre de la Universidad de Sonora y a su carrera, y que ahora, con más preparación, están en condiciones de ir por los primeros lugares en las siguientes competencias.
<br><br>
Los cuatro alumnos coincidieron en señalar que la mecatrónica les interesa sobremanera porque permite combinar varias ciencias, como las ingenierías mecánica y en sistemas, control y electrónica, entre otras áreas científicas, "en un mundo que hoy avanza vertiginosamente a través de la ciencia y tecnología". (JAR)  
