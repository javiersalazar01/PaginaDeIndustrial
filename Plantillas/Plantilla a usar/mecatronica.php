		<div id="egreso">
			<h2>Perfil de egreso</h2>
			<p>El Ingeniero Mecatrónico con su formación interdisciplinaria, tendrá la capacidad de analizar, diseñar, modelar, fabricar e implementar sistemas tecnológicos, integrando el diseño mecánico, con el control electrónico así como las capacidades de conocimiento informático, en diferentes campos del sector productivo y de servicios, de la pequeña, mediana y gran empresa, buscando la mejora continua de los procesos productivos.</p>
			<p>Su conocimiento del entorno tecnológico y su formación en planeación le permitirán dirigir y administrar proyectos mecatrónicos integrando soluciones a problemas en la industria automotriz, médica, de alimentos, aeroespacial, agricultura, electrodomésticos y de tecnologías de la información, entre otras, así como emprender actividades encaminadas a la generación de su propia empresa.</p>
			<p>La formación social y humanística del ingeniero mecatrónico y su responsabilidad en la utilización adecuada de los recursos naturales con un enfoque en desarrollo sustentable, complementa su formación integral.</p>
			<strong>Habilidades:</strong>
			<ul>
				<li>Diseñará sistemas de mecanismos que satisfagan las necesidades de innovación y adaptación en el sector industrial y comercial, en la automatización y robótica.</li>
				<li>Aplicará las teorías de control en procesos que requieran la interacción entre sistemas mecánicos, neumáticos, hidráulicos, eléctricos y electrónicos.</li>
				<li>Tendrá la capacidad de desarrollar soluciones mediante simulación, sistemas electrónicos y de procesamiento para los procesos de selección, pruebas de calidad y toma de decisiones que requiere la industria automatizada.</li>
				<li>Integrará con las tecnologías de información y software de ingeniería que proporcionan la amalgama con el cual se unen las piezas como un todo, así como integración espacial de los subsistemas en una sola unidad física.</li>
				<li>Desarrollará el software que define funciones del microprocesador con capacidad multifuncional.</li>
				<li>Ejercerá liderazgo para dirigir equipos de trabajo interdisciplinarios.</li>
				<li>Poseerá la capacidad de crear e innovar empleando los recursos existentes.</li>
				<li>Contará con espíritu emprendedor, siendo capaz de identificar nichos y oportunidades de generación de empresas o negocios relacionados con su perfil profesional.</li>
				<li>Contará con capacidad de decisión, en el esquema de la ética profesional.</li>
				<li>Estará comprometido con su entorno social y ambiental (en la proposición de soluciones que no afecten a la sociedad y al medio ambiente)</li>
				<li>Poseerá capacidad de asimilar tecnologías emergentes (toma el conocimiento para generar nueva tecnología a partir de tecnología existente)</li>
				<li>Será capaz de administrar proyectos mecatrónicos.</li>
				<li>Integrará equipos multidisciplinarios de trabajo, maximizando el uso de la tecnología en el diseño de sistemas y lograr con ello la reducción de costos de producción.</li>
