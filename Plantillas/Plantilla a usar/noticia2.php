          El próximo 27 de noviembre, la Universidad de Sonora será sede nacional de la aplicación del Examen General para el Egreso de la Licenciatura (EGEL), evaluación que podrán realizar egresados de esta casa de estudios y de cualquier institución de educación superior del país. <br><br>

El responsable institucional operativo de esta actividad, Marcelo Javier Gaviña, comentó que los pasantes de licenciatura interesados en presentar este examen para obtener su título o certificar sus conocimientos profesionales, tienen hasta el 28 de octubre para registrarse en el sitio http://www.dise.uson.mx/EGEL/inicio.htm <br><br>

Señaló que se trata de la última aplicación del año por parte del Centro Nacional de Evaluación para la Educación Superior (Ceneval), y además del campus Hermosillo, el examen también se aplicará en las instalaciones de la alma máter en Navojoa y Caborca.<br><br>

"Se aplicarán 26 exámenes distintos", subrayó, y añadió que para esta aplicación se espera el registro de 1,300 a 1,500 postulantes, y que para más información, los interesados pueden comunicarse a los teléfonos 259-2148 o 259-2254 antes del 28 de octubre, ya que después de ese día no se podrán atender dudas, pues la fecha de registro es nacional y acordada por Ceneval, no por la Universidad de Sonora.<br><br>

Marcelo Gaviña indicó que los requisitos y costos para presentar el EGEL están contemplados en el mencionado sitio de internet, y refirió que el costo de la evaluación es de 1,323 pesos, y para los egresados de la Universidad de Sonora es de 661.50 pesos.<br><br>

Mencionó que del total de postulantes, el 20% son pasantes provenientes de otras entidades del país, y los exámenes más solicitados son para las carreras de ingeniería industrial y de derecho. (LMA)

